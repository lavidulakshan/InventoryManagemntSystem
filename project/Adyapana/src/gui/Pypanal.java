    private void loadinvoice() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `invoice` "
                    + "INNER JOIN `subject` ON `invoice`.`subject_Subno` = `subject`.`Subno` "
                    + "INNER JOIN `month` ON `invoice`.`month_id` = `month`.`id` "
                    + "INNER JOIN `status` ON `invoice`.`status_id` = `status`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();

                v.add(resultSet.getString("id"));
                v.add(resultSet.getString("Student.Sno"));
                v.add(resultSet.getString("Student.name"));
                v.add(resultSet.getString("Subject.name"));
                v.add(resultSet.getString("Subject.price"));
                v.add(resultSet.getString("month.name"));
                v.add(resultSet.getString("status.name"));

                model.addRow(v);

            }
            jTable1.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


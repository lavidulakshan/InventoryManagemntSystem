    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            System.out.println("Select Row");
        } else {
            String id = String.valueOf(jTable1.getValueAt(selectedRow, 0));

            String sid = jTextField1.getText();
            String sname = jTextField2.getText();
            String fee = jTextField4.getText();
            String subject = String.valueOf(jComboBox2.getSelectedItem());
            String month = String.valueOf(jComboBox1.getSelectedItem());
            String status = String.valueOf(jComboBox3.getSelectedItem());

            try {

                int subid = SubMap.get(subject);
                int monthid = monthMap.get(month);
                int statusid = StatusMap.get(status);

                MySQL.execute("UPDATE `invoice` SET "
                        + "`Sid`='" + sid + "',"
                        + "`sname`='" + sname + "',"
                        + "`Subject_Subno`='" + subid + "',"
                        + "`fee`= '" + fee + "',"
                        + "`month_id`='" + monthid + "',"
                        + "`status_id`='" + statusid + "',"
                        + "WHERE `Sid`='" + id + "'");
                loadinvoice();
                reset();

                jTable1.setEnabled(true);
                jButton3.setEnabled(true);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


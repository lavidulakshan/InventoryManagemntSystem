    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            System.out.println("Please select row");
        } else {

            String id = String.valueOf(jTable1.getValueAt(selectedRow, 0));

            try {

                MySQL.execute("DELETE FROM `class` WHERE `Classno`='" + id + "'");

                loadclass();
                reset();
                jTable1.setEnabled(true);
                jButton3.setEnabled(true);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectRow = jTable1.getSelectedRow();

        if (selectRow == -1) {
            System.out.println("Select Row");
        } else {
            String id = String.valueOf(jTable1.getValueAt(selectRow, 0));

            String name = jTextField2.getText();
            String subject = String.valueOf(jComboBox1.getSelectedItem());
            String date = jTextField4.getText();
            String from = jTextField5.getText();
            String to = jTextField6.getText();

            try {
                int subid = subjectMap.get(subject);

                MySQL.execute("UPDATE `class` SET `Classno`='" + id + "',"
                        + "`name`='" + name + "',"
                        + "`date`='" + date + "',"
                        + "`from`='" + from + "',"
                        + "`to`='" + to + "',"
                        + "`Subject_Subno`='" + subid + "'"
                        + "WHERE `Classno`='" + id + "'");
                loadclass();
                reset();

                jTable1.setEnabled(true);
                jButton3.setEnabled(true);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed


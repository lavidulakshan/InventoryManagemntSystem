    private void loadclass() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` "
                    + "INNER JOIN `subject` ON `class`.`Subject_Subno` = `subject`.`Subno`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();

                v.add(resultSet.getString("Classno"));
                v.add(resultSet.getString("name"));
                v.add(resultSet.getString("Subject.name"));
                v.add(resultSet.getString("date"));
                v.add(resultSet.getString("from"));
                v.add(resultSet.getString("to"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


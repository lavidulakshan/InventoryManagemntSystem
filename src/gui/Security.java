    void loadusers() {
        try {
            ResultSet resultset = MySql.execute("SELECT * FROM `employee` WHERE `email`='" + UserLogin.getEmployeemail() + "'");

            if (resultset.next()) {
                String emptype = resultset.getString("employee_type_id");
                if (emptype.equals("1")) {
                    jTextField3.setText("Admin");
                } else if (emptype.equals("2")) {
                    jTextField3.setText("Cashier");

                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

